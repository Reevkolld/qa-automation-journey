// Запуск: node 03-this-binding.js
// Правила: 1) обычный вызов  2) метод объекта
//          3) явная привязка (call/apply/bind)  4) стрелочная (лексический this)
// =============================================================

// -------------------------------------------------------------
// Задача 1. Метод объекта vs «оторванный» метод
// ПРЕДСКАЗАНИЕ: что выведет user.greet()? _______Oleksandr
//              а оторванный greet()? _______ undefined
// -------------------------------------------------------------
const user = {
  name: "Oleksandr",
  greet() {
    return `Hi, I'm ${this.name}`;
  },
};
console.log("1a:", user.greet());
const g = user.greet;
console.log("1b:", g());   // что с this тут?

// -------------------------------------------------------------
// Задача 2. Стрелочная функция берёт this из окружения
// ПРЕДСКАЗАНИЕ: что выведет obj.regular() и obj.arrow()?
// -------------------------------------------------------------
const obj = {
  name: "obj",
  regular: function () {
    return this && this.name; //obj
  },
  arrow: () => {
    return this && this.name; // undefined, т.к. стрелочная функция не имеет своего this
  },
};
console.log("2a:", obj.regular());
console.log("2b:", obj.arrow());

// -------------------------------------------------------------
// Задача 3. Явная привязка через bind
// Привяжи greet к user2 так, чтобы оторванный вызов работал.
// -------------------------------------------------------------
const user2 = { name: "Bound User" };
const boundGreet = user.greet.bind(user2);
console.log("3:", boundGreet()); // ждём "Hi, I'm Bound User"

