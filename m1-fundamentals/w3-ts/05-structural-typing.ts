// Есть функция printPoint(p: { x: number; y: number }), которая печатает координаты. Покажи на коде два поведения TypeScript:

// Объект с лишним полем, переданный через переменную, — проходит (structural typing).
// Тот же объект как литерал напрямую в вызове — даёт ошибку компиляции (excess property check).
// Требования:

// В файле должны быть обе ситуации: рабочая (через переменную) и падающая (литерал) — падающую закомментируй, чтобы файл собирался, и подпиши комментом, какая ошибка ожидается.
// Ни одного any.
// Одним-двумя предложениями в разделе «Что выучил» объясни, зачем TS вообще делает excess property check только для литералов.
// Тип для p можешь описать инлайн или отдельно — реши сам. Суть задачи не в конструкции, а в том, чтобы увидеть разницу «переменная vs литерал».

function printPoint(p: { x: number; y: number }): void {
  console.log(`Point coordinates: (${p.x}, ${p.y})`);
}

function main(): void {
  const pointWithExtraField = { x: 10, y: 20, z: 30 }; // Лишнее поле z
  printPoint(pointWithExtraField); // Проходит компиляцию (structural typing)

  // printPoint({ x: 10, y: 20, z: 30 }); // Ошибка компиляции (excess property check)
}

main();

// TypeScript делает excess property check только для литералов, чтобы предотвратить случайные ошибки при передаче объектов с лишними полями,
// обеспечивая более строгую проверку типов в момент компиляции