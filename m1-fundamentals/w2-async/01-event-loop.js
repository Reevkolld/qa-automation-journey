// -------------------------------------------------------------
// Задача 1. Базовый порядок: sync vs macrotask vs microtask
// ПРЕДСКАЗАНИЕ: в каком порядке выведутся 4 строки? Порядок: 1а, 1d, 1c, 1b
// -------------------------------------------------------------
console.log("1a: sync");
setTimeout(() => console.log("1b: macrotask (setTimeout)"), 0);
Promise.resolve().then(() => console.log("1c: microtask (promise)"));
console.log("1d: sync");

// -------------------------------------------------------------
// Задача 2. Несколько setTimeout и несколько промисов вперемешку
// ПРЕДСКАЗАНИЕ: выпиши порядок вывода чисел 1-7 Порядок: 1,7, 3, 5, 2, 6, 4
// -------------------------------------------------------------
console.log(1);
setTimeout(() => console.log(2));
Promise.resolve().then(() => console.log(3));
Promise.resolve().then(() => setTimeout(() => console.log(4)));
Promise.resolve().then(() => console.log(5));
setTimeout(() => console.log(6));
console.log(7);

// -------------------------------------------------------------
// Задача 3. Микротаск, который сам добавляет новый микротаск
// ПРЕДСКАЗАНИЕ: "nested microtask" выведется до или после "after setTimeout"? 3b, 3c, 3a
// -------------------------------------------------------------
setTimeout(() => console.log("3a: after setTimeout"), 0);
Promise.resolve().then(() => {
  console.log("3b: first microtask");
  Promise.resolve().then(() => console.log("3c: nested microtask"));
});

// -------------------------------------------------------------
// Задача 4. Два setTimeout(fn, 0) подряд — порядок между собой
// ПРЕДСКАЗАНИЕ: "first" или "second" выведется раньше? 4a, 4b
// -------------------------------------------------------------
setTimeout(() => console.log("4a: first timeout"), 0);
setTimeout(() => console.log("4b: second timeout"), 0);

// -------------------------------------------------------------
// Задача 5 (QA-кейс). Имитация гонки: "сервер ответил" через промис
// vs "тест уже проверяет DOM" через setTimeout с такой же задержкой.
// ПРЕДСКАЗАНИЕ: что выведется раньше — "server responded" или "test checks DOM"? 5a, 5b
function fakeServerCall() {
  return Promise.resolve("data").then((data) => {
    console.log("5a: server responded with", data);
  });
}
fakeServerCall();
setTimeout(() => console.log("5b: test checks DOM"), 0);

